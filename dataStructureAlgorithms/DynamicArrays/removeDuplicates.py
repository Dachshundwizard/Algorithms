def remove_duplicates(lst):
    lst.sort()
    i = len(lst) - 1
    while i > 0:
        if lst[i] == lst[i - 1]:
            lst.pop(i)
        i -= 1
    return lst

print(remove_duplicates([5,5,4,3,2,2,1]))

