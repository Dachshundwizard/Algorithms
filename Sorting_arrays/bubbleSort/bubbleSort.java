package tutorial1;

import java.util.Arrays;

public class MainClass {

	public static void main(String[] args) {

		int[] array = new int[]{203, 99, 23, 6, 4, 3, 9, 7, 10, 2, 100, };      // We need an array to sort // Go through Array once, do a swap. Go through it again, do an other swap
		int tmp;									        					// Create a temp variable to handle the situation of storing a swapped index

		for(int i = 0; i < array.length-1; i++){            					// Iterate through our array. Swap elements if needed
			for(int j = 0; j < array.length-1-i; j++){      					// Each time we loop through, we do one less comparison
				if(array[j] > array[j+1]){                  					// If the current index is greater than the one after it...
					tmp = array[j];
					array[j] = array[j+1];
					array[j+1] = tmp;
				}
			}
		}
		System.out.println(Arrays.toString(array));
	}
}


